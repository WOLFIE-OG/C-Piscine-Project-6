#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
		ft_putchar(*str++);
}

int	main(int arg_n, char **arg_a)
{
	int	temp;

	temp = arg_n;
	ft_putstr(arg_a[0]);
	ft_putchar('\n');
	return (0);
}
